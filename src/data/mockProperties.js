// Transform a Domain API listing shape into the flat shape used throughout the app
export function transformDomainListing(domainListing) {
  const { listing } = domainListing
  const details = listing.propertyDetails
  return {
    domain_listing_id: String(listing.id),
    address: details.displayableAddress,
    suburb: details.suburb,
    state: details.state,
    postcode: details.postCode,
    price: listing.priceDetails.price,
    bedrooms: details.bedrooms,
    bathrooms: details.bathrooms,
    car_spaces: details.carspaces,
    land_size: details.landArea,
    property_type: details.propertyType,
    description: listing.description,
    images: listing.media.map((m) => m.url),
    listing_url: `https://www.domain.com.au/${listing.listingSlug}`,
    days_on_market: listing.daysOnMarket,
  }
}

export const transformedProperties = mockProperties.map(transformDomainListing)
